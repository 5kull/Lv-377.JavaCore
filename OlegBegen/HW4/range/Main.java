package com.company;

public class Main {

    public static void main(String[] args) {

        float a = 1.20f;
        float b = 4.4f;
        float c = 6.22f;

        if (a >= (-5) && a <= 5) {
            System.out.println("a: " + a + " - YES");
        } else System.out.println("a: " + a + " - NO");
        if (b >= (-5) && b <= 5) {
            System.out.println("b: " + b + " - YES");
        } else System.out.println("b: " + b +  " - NO");
        if (c >= (-5) && c <= 5) {
            System.out.println("c: " + c + " - YES");
        } else System.out.println("c: " + c + " - NO");
    }

}
